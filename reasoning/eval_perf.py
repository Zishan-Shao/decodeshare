def load_model_and_tokenizer(
    model_name: str,
    device: str,
    dtype: str,
    trust_remote_code: bool = False,
    device_map: Optional[str] = None,
    max_memory_per_gpu_gb: float = 0.0,
    cpu_offload_gb: float = 0.0,
    max_memory_map: str = "",
):
    if dtype == "fp32":
        torch_dtype = torch.float32
    elif dtype == "fp16":
        torch_dtype = torch.float16
    elif dtype == "bf16":
        torch_dtype = torch.bfloat16
    else:
        raise ValueError(f"Unknown dtype: {dtype}")

    model_kwargs = {"torch_dtype": torch_dtype, "trust_remote_code": trust_remote_code}
    tok_kwargs = {"trust_remote_code": trust_remote_code}

    if device_map is None:
        device_map = None
    else:
        device_map_s = str(device_map).strip()
        if device_map_s.lower() in {"", "none", "null", "false"}:
            device_map = None
        else:
            device_map = device_map_s
    if device_map is not None:
        model_kwargs["device_map"] = device_map
        model_kwargs["low_cpu_mem_usage"] = True

        parsed_max_mem = _parse_max_memory_map(max_memory_map)
        if parsed_max_mem:
            if not torch.cuda.is_available():
                raise RuntimeError("device_map was requested but CUDA is unavailable.")
            model_kwargs["max_memory"] = parsed_max_mem
        elif float(max_memory_per_gpu_gb) > 0.0:
            if not torch.cuda.is_available():
                raise RuntimeError("device_map was requested but CUDA is unavailable.")
            max_mem = {i: f"{int(float(max_memory_per_gpu_gb))}GiB" for i in range(torch.cuda.device_count())}
            if float(cpu_offload_gb) > 0.0:
                max_mem["cpu"] = f"{int(float(cpu_offload_gb))}GiB"
            model_kwargs["max_memory"] = max_mem
        elif float(cpu_offload_gb) > 0.0:
            model_kwargs["max_memory"] = {"cpu": f"{int(float(cpu_offload_gb))}GiB"}

    model = AutoModelForCausalLM.from_pretrained(model_name, **model_kwargs)
    tok = AutoTokenizer.from_pretrained(model_name, **tok_kwargs)
    if isinstance(tok, bool):
        tok = LlamaTokenizer.from_pretrained(model_name, **tok_kwargs)

    tok.padding_side = "left"
    if tok.pad_token is None:
        tok.pad_token = tok.eos_token

    if device_map is None:
        model = model.to(device)
    model.eval()
    if hasattr(model.config, "use_cache"):
        model.config.use_cache = True
    return model, tok


