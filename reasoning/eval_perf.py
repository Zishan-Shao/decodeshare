def orthonormalize_np(basis: np.ndarray) -> np.ndarray:
    q, _ = np.linalg.qr(basis.astype(np.float32, copy=False))
    return q.astype(np.float32, copy=False)


def random_orthonormal_basis_np(dim: int, k: int, seed: int) -> np.ndarray:
    rng = np.random.default_rng(seed)
    a = rng.standard_normal((dim, k), dtype=np.float32)
    return orthonormalize_np(a)


def balanced_concat(task_to_states: Dict[str, np.ndarray], seed: int) -> Tuple[np.ndarray, int]:
    sizes = {t: int(v.shape[0]) for t, v in task_to_states.items()}
    n_min = min(sizes.values())
    out = []
    for t, X in task_to_states.items():
        rng = np.random.default_rng(stable_int_seed(seed, t, "bal"))
        if X.shape[0] > n_min:
            idx = rng.choice(X.shape[0], size=n_min, replace=False)
            out.append(X[idx])
        else:
            out.append(X)
    return np.concatenate(out, axis=0), int(n_min)


def balance_task_states(task_to_states: Dict[str, np.ndarray], seed: int) -> Tuple[Dict[str, np.ndarray], int]:
    """
    Subsample each task's states to the same count (the min across tasks), to avoid
    overweighting tasks that happened to produce more decode steps/states.
    """
    sizes = {t: int(v.shape[0]) for t, v in task_to_states.items()}
    n_min = min(sizes.values())
    balanced: Dict[str, np.ndarray] = {}
    for t, X in task_to_states.items():
        rng = np.random.default_rng(stable_int_seed(seed, t, "balance"))
        if X.shape[0] > n_min:
            idx = rng.choice(X.shape[0], size=n_min, replace=False)
            balanced[t] = X[idx]
        else:
            balanced[t] = X
    return balanced, int(n_min)


def compute_shared_basis_from_states(
    task_states: Dict[str, np.ndarray],
    *,
    pca_var: float,
    min_dim: int,
    max_dim: int,
    tau: float,
    m_shared: str,
    seed: int,
) -> Tuple[np.ndarray, List[int], Dict[str, Any]]:
    """
    Compute joint subspace + shared indices from task->states dict (single layer).
    Uses your project utilities: compute_cross_task_subspace + find_fully_shared_basis_improved.
    """
    # Balance task state counts to avoid overweighting tasks with more states
    task_states_bal, n_bal = balance_task_states(task_states, seed)
    tasks = list(task_states_bal.keys())
    # compute_cross_task_subspace expects dict: task -> {layer_idx -> states}
    task_dict = {t: {0: task_states_bal[t]} for t in tasks}

    joint_subspace, cross_dim, contributions, full_pca_info = compute_cross_task_subspace(
        task_dict,
        variance_threshold=float(pca_var),
        min_dim=int(min_dim),
        max_dim=int(max_dim),
        return_full_pca=True,
    )
    if joint_subspace is None or int(cross_dim) <= 0:
        raise RuntimeError("Failed to compute cross-task subspace")

    min_tasks_shared = len(tasks) if str(m_shared).strip().lower() == "all" else int(m_shared)
    shared_idx = find_fully_shared_basis_improved(
        contributions,
        tasks,
        int(cross_dim),
        min_tasks_shared=int(min_tasks_shared),
        relative_threshold=float(tau),
        top_k_components=int(cross_dim),
    )

    extra = {
        "tasks_used": tasks,
        "n_balanced": int(n_bal),
        "cross_dim": int(cross_dim),
        "task_contributions": contributions,
        "full_pca_info": full_pca_info,
    }
    return joint_subspace.astype(np.float32, copy=False), shared_idx, extra


def build_shared_basis_from_joint(joint: np.ndarray, shared_idx: List[int], k: int) -> np.ndarray:
    if int(k) <= 0:
        raise ValueError("k must be positive")
    if len(shared_idx) < int(k):
        raise ValueError(f"Need at least {k} shared components, got {len(shared_idx)}")
    idx = sorted(shared_idx)[: int(k)]
    return orthonormalize_np(joint[:, idx])


def subspace_similarity(Qa: np.ndarray, Qb: np.ndarray) -> Dict[str, float]:
    M = Qa.T @ Qb
    s = np.linalg.svd(M, compute_uv=False)
    s = np.clip(s, 0.0, 1.0)
    return {
        "max_cos": float(np.max(s)) if s.size else float("nan"),
        "mean_cos": float(np.mean(s)) if s.size else float("nan"),
        "min_cos": float(np.min(s)) if s.size else float("nan"),
        "fro_norm": float(np.linalg.norm(M, ord="fro")),
    }


def energy_ratio_stats(states: np.ndarray, Q: np.ndarray) -> Dict[str, float]:
    eps = 1e-12
    H = states.astype(np.float32, copy=False)
    proj = H @ Q
    num = np.sum(proj * proj, axis=1)
    den = np.sum(H * H, axis=1) + eps
    r = num / den
    return {"mean": float(np.mean(r)), "p50": float(np.percentile(r, 50)), "p95": float(np.percentile(r, 95))}


@dataclass
class SharedBases:
    Q_decode_full: np.ndarray
    Q_prefill_full: np.ndarray
    Q_decode_k: np.ndarray
    Q_prefill_k: np.ndarray
    Q_rand_k: np.ndarray
    k_decode: int
    k_prefill: int
    k_eval: int
    extra_decode: Dict[str, Any]
    extra_prefill: Dict[str, Any]
    similarity_full: Dict[str, float]
    similarity_k: Dict[str, float]
    energy: Dict[str, Any]


@torch.no_grad()
def compute_decode_prefill_shared_bases(
    model,
    tok,
    sub_by: Dict[str, List[Example]],
    *,
    layer_idx: int,
    batch_size: int,
    max_prompt_len: int,
    calib_decode_max_new_tokens: int,
    per_task_max_states: int,
    pca_var: float,
    min_dim: int,
    max_dim: int,
    tau: float,
    m_shared: str,
    seed: int,
    match_state_count: bool = False,
    k_eval: int = 0,
) -> SharedBases:
    """
    Compute shared bases for decode and prefill collections.
    Returns both full shared bases and dimension-matched (or k_eval) bases.
    """
    layers, _ = get_model_layers(model)
    if int(layer_idx) < 0 or int(layer_idx) >= len(layers):
        raise ValueError(f"layer_idx={layer_idx} out of range: num_layers={len(layers)}")

    # Collect decode states
    decode_col = DecodeLastTokenCollector([int(layer_idx)])
    handles = [layers[int(layer_idx)].register_forward_hook(decode_col.make_hook(int(layer_idx)))]
    decode_task_states: Dict[str, np.ndarray] = {}
    try:
        for task, sub_exs in sub_by.items():
            decode_col.set_current_task(task)
            prompts = [ex.prompt for ex in sub_exs]
            collect_decode_states(
                model, tok, prompts, decode_col,
                batch_size=batch_size,
                max_new_tokens=calib_decode_max_new_tokens,
                max_prompt_len=max_prompt_len,
                decoding="greedy",
                temperature=1.0,
                top_p=1.0,
                top_k=0,
            )
            X = decode_col.get(task, int(layer_idx))
            if X is None:
                raise RuntimeError(f"No decode states for task={task}")
            X = subsample_rows_np(X, per_task_max_states, seed=stable_int_seed(seed, task, "decode"))
            decode_task_states[task] = X
    finally:
        for h in handles:
            try:
                h.remove()
            except Exception:
                pass
        decode_col.set_capture(False, None)

    # Collect prefill states
    pre_col = PrefillLastTokenCollector([int(layer_idx)])
    handles = [layers[int(layer_idx)].register_forward_hook(pre_col.make_hook(int(layer_idx)))]
    pre_task_states: Dict[str, np.ndarray] = {}
    try:
        for task, sub_exs in sub_by.items():
            pre_col.set_current_task(task)
            prompts = [ex.prompt for ex in sub_exs]
            collect_prefill_states(
                model, tok, prompts, pre_col,
                batch_size=batch_size,
                max_prompt_len=max_prompt_len,
            )
            X = pre_col.get(task, int(layer_idx))
            if X is None:
                raise RuntimeError(f"No prefill states for task={task}")
            X = subsample_rows_np(X, per_task_max_states, seed=stable_int_seed(seed, task, "prefill"))
            pre_task_states[task] = X
    finally:
        for h in handles:
            try:
                h.remove()
            except Exception:
                pass

    # Optional state-count matching
    if bool(match_state_count):
        n_decode_min = min(v.shape[0] for v in decode_task_states.values())
        n_prefill_min = min(v.shape[0] for v in pre_task_states.values())
        n_match = min(int(n_decode_min), int(n_prefill_min))
        decode_task_states = {t: subsample_rows_np(X, n_match, seed=stable_int_seed(seed, t, "decode_match")) for t, X in decode_task_states.items()}
        pre_task_states = {t: subsample_rows_np(X, n_match, seed=stable_int_seed(seed, t, "prefill_match")) for t, X in pre_task_states.items()}

    # Compute joint subspaces
    joint_decode, shared_idx_decode, extra_decode = compute_shared_basis_from_states(
        decode_task_states, pca_var=pca_var, min_dim=min_dim, max_dim=max_dim, tau=tau, m_shared=m_shared, seed=seed + 100
    )
    joint_prefill, shared_idx_prefill, extra_prefill = compute_shared_basis_from_states(
        pre_task_states, pca_var=pca_var, min_dim=min_dim, max_dim=max_dim, tau=tau, m_shared=m_shared, seed=seed + 200
    )

    k_decode = len(shared_idx_decode)
    k_prefill = len(shared_idx_prefill)
    k_match_raw = min(k_decode, k_prefill)
    if k_match_raw <= 0:
        raise RuntimeError("No shared components found (k_match<=0)")

    k_use = int(k_match_raw)
    if int(k_eval) > 0:
        if int(k_eval) > k_match_raw:
            k_use = int(k_match_raw)
        else:
            k_use = int(k_eval)

    # Build bases
    hidden_dim = joint_decode.shape[0]
    Q_decode_full = build_shared_basis_from_joint(joint_decode, shared_idx_decode, k_decode)
    Q_prefill_full = build_shared_basis_from_joint(joint_prefill, shared_idx_prefill, k_prefill)
    Q_decode_k = build_shared_basis_from_joint(joint_decode, shared_idx_decode, k_use)
    Q_prefill_k = build_shared_basis_from_joint(joint_prefill, shared_idx_prefill, k_use)
    Q_rand_k = random_orthonormal_basis_np(hidden_dim, k_use, seed=stable_int_seed(seed, "rand", k_use))

    # Diagnostics
    similarity_full = subspace_similarity(Q_decode_full, Q_prefill_full)
    similarity_k = subspace_similarity(Q_decode_k, Q_prefill_k)

    # Energy ratios: sample pools
    decode_pool = np.concatenate([subsample_rows_np(X, 4000, seed=stable_int_seed(seed, t, "er_d")) for t, X in decode_task_states.items()], axis=0)
    pre_pool = np.concatenate([subsample_rows_np(X, 4000, seed=stable_int_seed(seed, t, "er_p")) for t, X in pre_task_states.items()], axis=0)

    energy = {
        "full": {
            "decode_on_decode": energy_ratio_stats(decode_pool, Q_decode_full),
            "prefill_on_decode": energy_ratio_stats(decode_pool, Q_prefill_full),
            "prefill_on_prefill": energy_ratio_stats(pre_pool, Q_prefill_full),
            "decode_on_prefill": energy_ratio_stats(pre_pool, Q_decode_full),
        },
        "k": {
            "decode_on_decode": energy_ratio_stats(decode_pool, Q_decode_k),
            "prefill_on_decode": energy_ratio_stats(decode_pool, Q_prefill_k),
            "rand_on_decode": energy_ratio_stats(decode_pool, Q_rand_k),
            "prefill_on_prefill": energy_ratio_stats(pre_pool, Q_prefill_k),
            "decode_on_prefill": energy_ratio_stats(pre_pool, Q_decode_k),
        },
    }

    return SharedBases(
        Q_decode_full=Q_decode_full,
        Q_prefill_full=Q_prefill_full,
        Q_decode_k=Q_decode_k,
        Q_prefill_k=Q_prefill_k,
        Q_rand_k=Q_rand_k,
        k_decode=int(k_decode),
        k_prefill=int(k_prefill),
        k_eval=int(k_use),
        extra_decode=extra_decode,
        extra_prefill=extra_prefill,
        similarity_full=similarity_full,
        similarity_k=similarity_k,
        energy=energy,
    )


