# Sampling helpers (top-k, top-p)
# -----------------------------------------------------------------------------
def top_p_filtering(logits: torch.Tensor, top_p: float) -> torch.Tensor:
    if top_p <= 0.0 or top_p >= 1.0:
        return logits
    sorted_logits, sorted_idx = torch.sort(logits, descending=True, dim=-1)
    probs = torch.softmax(sorted_logits, dim=-1)
    cumprobs = torch.cumsum(probs, dim=-1)
    mask = cumprobs > top_p
    mask[..., 0] = False
    sorted_logits = sorted_logits.masked_fill(mask, float("-inf"))
    filtered = torch.full_like(logits, float("-inf"))
    filtered.scatter_(dim=-1, index=sorted_idx, src=sorted_logits)
    return filtered


def top_k_filtering(logits: torch.Tensor, top_k: int) -> torch.Tensor:
    if top_k is None or top_k <= 0:
        return logits
    top_k = min(int(top_k), logits.size(-1))
    values, _ = torch.topk(logits, top_k, dim=-1)
    min_values = values[:, -1].unsqueeze(-1)
    return torch.where(logits < min_values, torch.full_like(logits, float("-inf")), logits)


def choose_next_token(
    logits: torch.Tensor,
    *,
    decoding: str,
    temperature: float,
    top_p: float,
    top_k: int,
    eos_token_id: int,
    ban_eos: bool = False,
) -> torch.Tensor:
    """Return next_token [B,1]."""
    assert decoding in ["greedy", "sample"]
    if ban_eos:
        logits = logits.clone()
        logits[:, eos_token_id] = float("-inf")

    if decoding == "greedy":
        return torch.argmax(logits, dim=-1, keepdim=True)

    lt = logits / max(float(temperature), 1e-6)
    lt = top_k_filtering(lt, top_k)
    lt = top_p_filtering(lt, top_p)
    probs = torch.softmax(lt, dim=-1)
    return torch.multinomial(probs, num_samples=1)


