@torch.no_grad()
def precompute_fc_warmup_tokens(
    model,
    tok,
    prompts: List[str],
    *,
    warmup_tokens: int,
    batch_size: int,
    max_prompt_len: int,
    decoding: str,
    temperature: float,
    top_p: float,
    top_k: int,
    ban_eos: bool,
    seed: int,
) -> np.ndarray:
    """Generate W warmup tokens under baseline (no intervention). Returns [N,W] int64 on CPU."""
    assert int(warmup_tokens) >= 0
    W = int(warmup_tokens)
    if W == 0:
        return np.zeros((len(prompts), 0), dtype=np.int64)

    device = model_input_device(model)
    eos = tok.eos_token_id
    model.eval()

    if decoding == "sample":
        torch.manual_seed(seed)
        if torch.cuda.is_available():
            torch.cuda.manual_seed_all(seed)

    out_tokens = np.zeros((len(prompts), W), dtype=np.int64)

    for i in tqdm(range(0, len(prompts), batch_size), desc=f"WarmupGen(W={W})"):
        batch = prompts[i : i + batch_size]
        inputs = tok(batch, return_tensors="pt", padding=True, truncation=True, max_length=max_prompt_len).to(device)
        ids = inputs["input_ids"]
        attn = inputs["attention_mask"]
        B = ids.shape[0]

        past, logits = cache_decode_aligned_boundary(model, ids, attn)

        toks = []
        for _ in range(W):
            next_tok = choose_next_token(
                logits,
                decoding=decoding,
                temperature=temperature,
                top_p=top_p,
                top_k=top_k,
                eos_token_id=eos,
                ban_eos=bool(ban_eos),
            )
            toks.append(next_tok)
            attn = torch.cat([attn, torch.ones((B, 1), device=device, dtype=attn.dtype)], dim=1)
            out = model(input_ids=next_tok, attention_mask=attn, use_cache=True, past_key_values=past)
            logits = out.logits[:, -1, :]
            past = out.past_key_values

        toks_mat = torch.cat(toks, dim=1)  # [B,W]
        out_tokens[i : i + B, :] = toks_mat.detach().cpu().numpy().astype(np.int64, copy=False)

    return out_tokens


