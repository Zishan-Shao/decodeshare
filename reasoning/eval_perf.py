@torch.no_grad()
def cache_decode_aligned_boundary(model, ids: torch.Tensor, attn: torch.Tensor):
    """
    Returns (past_key_values, logits_next) such that the last prompt token
    is processed with seq_len==1 (decode step). This makes decode-only hooks
    affect the first decision token right after the prompt.
    """
    if ids.ndim != 2:
        raise ValueError(f"ids must be 2D [B,T], got {ids.shape}")
    B, T = ids.shape
    if T <= 0:
        raise ValueError("Empty prompt")
    if T == 1:
        out1 = model(input_ids=ids, attention_mask=attn, use_cache=True)
        return out1.past_key_values, out1.logits[:, -1, :]

    out0 = model(input_ids=ids[:, :-1], attention_mask=attn[:, :-1], use_cache=True)
    out1 = model(input_ids=ids[:, -1:], attention_mask=attn, use_cache=True, past_key_values=out0.past_key_values)
    return out1.past_key_values, out1.logits[:, -1, :]


