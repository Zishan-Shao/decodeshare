def evaluate_tasks_once(
    model,
    tok,
    eval_by: Dict[str, List[Example]],
    *,
    tasks: List[str],
    layer_idx: int,
    bases: SharedBases,
    alpha_remove: float,
    batch_size: int,
    max_prompt_len: int,
    do_generation: bool,
    # forced-choice settings
    fc_warmup_by_task: Dict[str, np.ndarray],
    fc_answer_prefix: str,
    fc_prefix_mode: str,
    fc_save_scores: bool,
    # generation settings
    gen_modes: List[str],
    gen_max_new_tokens: int,
    gen_temperature: float,
    gen_top_p: float,
    gen_top_k: int,
    gen_seed: int,
    # stats
    bootstrap_iters: int,
    perm_iters: int,
    ci_alpha: float,
    seed: int,
) -> Dict[str, Any]:
    """
    Evaluate each task under baseline/decode_shared/prefill_shared/random using either
    forced-choice or generation, returning per-task results and paired stats on decode-prefill.
    """
    results: Dict[str, Any] = {}
    layer_indices = [int(layer_idx)]

    for task in tasks:
        exs = eval_by[task]
        n = len(exs)

        use_generation = bool(do_generation) or (len(candidate_strings(task)) == 0)
        protocol = "generation" if use_generation else "forced_choice"

        # Baseline run(s)
        baseline_detail: Dict[str, Any]
        baseline_correct: np.ndarray

        if use_generation:
            # For generation, we may evaluate multiple decoding modes (greedy + sample)
            baseline_detail = {}
            baseline_correct = None  # type: ignore
            # We'll use the first mode as the main for paired tests/tables; store others as extras.
            main_mode = gen_modes[0]
            for mode in gen_modes:
                run = generation_eval(
                    model, tok, exs, task,
                    layer_indices=layer_indices,
                    basis_np=None,
                    alpha=0.0,
                    batch_size=batch_size,
                    max_prompt_len=max_prompt_len,
                    max_new_tokens=gen_max_new_tokens,
                    decoding=mode,
                    temperature=gen_temperature,
                    top_p=gen_top_p,
                    top_k=gen_top_k,
                    seed=stable_int_seed(seed, gen_seed, task, "gen_base", mode),
                )
                baseline_detail[mode] = run
                if mode == main_mode:
                    baseline_correct = np.array(run["correct"], dtype=np.float32)
        else:
            warm_ids = fc_warmup_by_task.get(task, None)
            baseline_detail = forced_choice_logprob_eval(
                model, tok, exs, task,
                layer_indices=layer_indices,
                basis_np=None,
                alpha=0.0,
                batch_size=batch_size,
                max_prompt_len=max_prompt_len,
                warmup_token_ids=warm_ids,
                answer_prefix=fc_answer_prefix,
                prefix_mode=fc_prefix_mode,
                save_scores=bool(fc_save_scores),
            )
            baseline_correct = np.array(baseline_detail["correct"], dtype=np.float32)

        b_acc, b_lo, b_hi = bootstrap_ci_mean(
            baseline_correct, bootstrap_iters, ci_alpha, seed=stable_int_seed(seed, task, protocol, "baseline", alpha_remove)
        )

        # Helper to eval a condition
        def _eval_condition(name: str, Q: Optional[np.ndarray]) -> Tuple[Dict[str, Any], np.ndarray, Tuple[float, float, float]]:
            if use_generation:
                main_mode = gen_modes[0]
                detail = {}
                main_correct = None  # type: ignore
                for mode in gen_modes:
                    run = generation_eval(
                        model, tok, exs, task,
                        layer_indices=layer_indices,
                        basis_np=Q,
                        alpha=float(alpha_remove),
                        batch_size=batch_size,
                        max_prompt_len=max_prompt_len,
                        max_new_tokens=gen_max_new_tokens,
                        decoding=mode,
                        temperature=gen_temperature,
                        top_p=gen_top_p,
                        top_k=gen_top_k,
                        seed=stable_int_seed(seed, gen_seed, task, name, mode, alpha_remove),
                    )
                    detail[mode] = run
                    if mode == main_mode:
                        main_correct = np.array(run["correct"], dtype=np.float32)
                m, lo, hi = bootstrap_ci_mean(main_correct, bootstrap_iters, ci_alpha, seed=stable_int_seed(seed, task, protocol, name, alpha_remove))
                return detail, main_correct, (m, lo, hi)
            else:
                warm_ids = fc_warmup_by_task.get(task, None)
                detail = forced_choice_logprob_eval(
                    model, tok, exs, task,
                    layer_indices=layer_indices,
                    basis_np=Q,
                    alpha=float(alpha_remove),
                    batch_size=batch_size,
                    max_prompt_len=max_prompt_len,
                    warmup_token_ids=warm_ids,
                    answer_prefix=fc_answer_prefix,
                    prefix_mode=fc_prefix_mode,
                    save_scores=bool(fc_save_scores),
                )
                arr = np.array(detail["correct"], dtype=np.float32)
                m, lo, hi = bootstrap_ci_mean(arr, bootstrap_iters, ci_alpha, seed=stable_int_seed(seed, task, protocol, name, alpha_remove))
                return detail, arr, (m, lo, hi)

        # Conditions
        dec_detail, dec_arr, (d_acc, d_lo, d_hi) = _eval_condition("decode_shared", bases.Q_decode_k)
        pre_detail, pre_arr, (p_acc, p_lo, p_hi) = _eval_condition("prefill_shared", bases.Q_prefill_k)
        rnd_detail, rnd_arr, (r_acc, r_lo, r_hi) = _eval_condition("random", bases.Q_rand_k)

        # Paired decode vs prefill (using correct arrays)
        stat = summarize_paired(
            baseline_correct=pre_arr,
            treat_correct=dec_arr,
            bootstrap_iters=bootstrap_iters,
            perm_iters=perm_iters,
            alpha=ci_alpha,
            seed=stable_int_seed(seed, task, protocol, "paired", alpha_remove),
        )

        results[task] = {
            "protocol": protocol,
            "n": int(n),
            "baseline": {"acc": b_acc, "ci": [b_lo, b_hi], "detail": baseline_detail},
            "decode_shared": {"acc": d_acc, "ci": [d_lo, d_hi], "k": bases.k_eval, "detail": dec_detail},
            "prefill_shared": {"acc": p_acc, "ci": [p_lo, p_hi], "k": bases.k_eval, "detail": pre_detail},
            "random": {"acc": r_acc, "ci": [r_lo, r_hi], "k": bases.k_eval, "detail": rnd_detail},
            "paired": {"decode_minus_prefill": stat},
        }

    return results



def evaluate_tasks_alpha_sweep(
    model,
    tok,
    eval_by: Dict[str, List[Example]],
    *,
    tasks: List[str],
    layer_idx: int,
    bases: SharedBases,
    alphas: List[float],
    batch_size: int,
    max_prompt_len: int,
    do_generation: bool,
    # forced-choice settings
    fc_warmup_by_task: Dict[str, np.ndarray],
    fc_answer_prefix: str,
    fc_prefix_mode: str,
    fc_save_scores: bool,
    # generation settings
    gen_modes: List[str],
    gen_max_new_tokens: int,
    gen_temperature: float,
    gen_top_p: float,
    gen_top_k: int,
    gen_seed: int,
    # stats
    bootstrap_iters: int,
    perm_iters: int,
    ci_alpha: float,
    seed: int,
) -> Dict[str, Dict[str, Any]]:
    """
    Efficient alpha sweep:
      - Computes BASELINE once per task (reused across all alphas).
      - For each alpha, computes decode/prefill/random and paired stats.

    Returns:
      runs[alpha_str][task] -> task result dict (same schema as evaluate_tasks_once).
    """
    if not alphas:
        raise ValueError("alphas must be non-empty")

    layer_indices = [int(layer_idx)]

    # -------------------------
    # Baseline cache (per task)
    # -------------------------
    baseline_cache: Dict[str, Dict[str, Any]] = {}
    baseline_correct_cache: Dict[str, np.ndarray] = {}
    protocol_cache: Dict[str, str] = {}
    n_cache: Dict[str, int] = {}

    for task in tasks:
        exs = eval_by[task]
        n_cache[task] = len(exs)

        use_generation = bool(do_generation) or (len(candidate_strings(task)) == 0)
        protocol = "generation" if use_generation else "forced_choice"
        protocol_cache[task] = protocol

        if use_generation:
            detail = {}
            main_mode = gen_modes[0]
            main_correct = None  # type: ignore
            for mode in gen_modes:
                run = generation_eval(
                    model, tok, exs, task,
                    layer_indices=layer_indices,
                    basis_np=None,
                    alpha=0.0,
                    batch_size=batch_size,
                    max_prompt_len=max_prompt_len,
                    max_new_tokens=gen_max_new_tokens,
                    decoding=mode,
                    temperature=gen_temperature,
                    top_p=gen_top_p,
                    top_k=gen_top_k,
                    seed=stable_int_seed(seed, gen_seed, task, "gen_base", mode),
                )
                detail[mode] = run
                if mode == main_mode:
                    main_correct = np.array(run["correct"], dtype=np.float32)
            baseline_detail = detail
            baseline_correct = main_correct
        else:
            warm_ids = fc_warmup_by_task.get(task, None)
            baseline_detail = forced_choice_logprob_eval(
                model, tok, exs, task,
                layer_indices=layer_indices,
                basis_np=None,
                alpha=0.0,
                batch_size=batch_size,
                max_prompt_len=max_prompt_len,
                warmup_token_ids=warm_ids,
                answer_prefix=fc_answer_prefix,
                prefix_mode=fc_prefix_mode,
                save_scores=bool(fc_save_scores),
            )
            baseline_correct = np.array(baseline_detail["correct"], dtype=np.float32)

        b_acc, b_lo, b_hi = bootstrap_ci_mean(
            baseline_correct, bootstrap_iters, ci_alpha, seed=stable_int_seed(seed, task, protocol, "baseline")
        )

        baseline_cache[task] = {"acc": b_acc, "ci": [b_lo, b_hi], "detail": baseline_detail}
        baseline_correct_cache[task] = baseline_correct

    # -------------------------
    # Sweep over alphas
    # -------------------------
    runs: Dict[str, Dict[str, Any]] = {}

    for alpha_remove in alphas:
        eval_results: Dict[str, Any] = {}

        for task in tasks:
            exs = eval_by[task]
            protocol = protocol_cache[task]
            use_generation = (protocol == "generation")

            baseline_entry = baseline_cache[task]
            baseline_correct = baseline_correct_cache[task]

            def _eval_condition(name: str, Q: Optional[np.ndarray]) -> Tuple[Dict[str, Any], np.ndarray, Tuple[float, float, float]]:
                if use_generation:
                    main_mode = gen_modes[0]
                    detail = {}
                    main_correct = None  # type: ignore
                    for mode in gen_modes:
                        run = generation_eval(
                            model, tok, exs, task,
                            layer_indices=layer_indices,
                            basis_np=Q,
                            alpha=float(alpha_remove),
                            batch_size=batch_size,
                            max_prompt_len=max_prompt_len,
                            max_new_tokens=gen_max_new_tokens,
                            decoding=mode,
                            temperature=gen_temperature,
                            top_p=gen_top_p,
                            top_k=gen_top_k,
                            seed=stable_int_seed(seed, gen_seed, task, name, mode, alpha_remove),
                        )
                        detail[mode] = run
                        if mode == main_mode:
                            main_correct = np.array(run["correct"], dtype=np.float32)
                    m, lo, hi = bootstrap_ci_mean(
                        main_correct, bootstrap_iters, ci_alpha, seed=stable_int_seed(seed, task, protocol, name, alpha_remove)
                    )
                    return detail, main_correct, (m, lo, hi)
                else:
                    warm_ids = fc_warmup_by_task.get(task, None)
                    detail = forced_choice_logprob_eval(
                        model, tok, exs, task,
                        layer_indices=layer_indices,
                        basis_np=Q,
                        alpha=float(alpha_remove),
                        batch_size=batch_size,
                        max_prompt_len=max_prompt_len,
                        warmup_token_ids=warm_ids,
                        answer_prefix=fc_answer_prefix,
                        prefix_mode=fc_prefix_mode,
                        save_scores=bool(fc_save_scores),
                    )
                    arr = np.array(detail["correct"], dtype=np.float32)
                    m, lo, hi = bootstrap_ci_mean(
                        arr, bootstrap_iters, ci_alpha, seed=stable_int_seed(seed, task, protocol, name, alpha_remove)
                    )
                    return detail, arr, (m, lo, hi)

            dec_detail, dec_arr, (d_acc, d_lo, d_hi) = _eval_condition("decode_shared", bases.Q_decode_k)
            pre_detail, pre_arr, (p_acc, p_lo, p_hi) = _eval_condition("prefill_shared", bases.Q_prefill_k)
            rnd_detail, rnd_arr, (r_acc, r_lo, r_hi) = _eval_condition("random", bases.Q_rand_k)

            stat = summarize_paired(
                baseline_correct=pre_arr,
                treat_correct=dec_arr,
                bootstrap_iters=bootstrap_iters,
                perm_iters=perm_iters,
                alpha=ci_alpha,
                seed=stable_int_seed(seed, task, protocol, "paired", alpha_remove),
            )

            eval_results[task] = {
                "protocol": protocol,
                "n": int(n_cache[task]),
                "baseline": baseline_entry,
                "decode_shared": {"acc": d_acc, "ci": [d_lo, d_hi], "k": bases.k_eval, "detail": dec_detail},
                "prefill_shared": {"acc": p_acc, "ci": [p_lo, p_hi], "k": bases.k_eval, "detail": pre_detail},
                "random": {"acc": r_acc, "ci": [r_lo, r_hi], "k": bases.k_eval, "detail": rnd_detail},
                "paired": {"decode_minus_prefill": stat},
            }

        runs[str(alpha_remove)] = eval_results

    return runs



def build_summary_table(eval_results: Dict[str, Any], *, k: int) -> str:
    rows = []
    for task, r in eval_results.items():
        stat = r["paired"]["decode_minus_prefill"]
        rows.append([
            task,
            r["protocol"],
            str(r["n"]),
            fmt_acc(r["baseline"]["acc"], r["baseline"]["ci"][0], r["baseline"]["ci"][1]),
            fmt_acc(r["decode_shared"]["acc"], r["decode_shared"]["ci"][0], r["decode_shared"]["ci"][1]),
            fmt_acc(r["prefill_shared"]["acc"], r["prefill_shared"]["ci"][0], r["prefill_shared"]["ci"][1]),
            fmt_acc(r["random"]["acc"], r["random"]["ci"][0], r["random"]["ci"][1]),
            f"{stat['mean_diff']*100:+.1f} [{stat['ci_low']*100:+.1f}, {stat['ci_high']*100:+.1f}]",
            f"{stat['p_value']:.3g}",
        ])
    header = [
        "Task", "Protocol", "n",
        "Baseline",
        f"Decode-shared (k={k})",
        f"Prefill-shared (k={k})",
        f"Random (k={k})",
        "Δ(Decode-Prefill) [CI]",
        "p",
    ]
    return md_table(rows, header)
