def model_input_device(model) -> torch.device:
    """
    Best-effort input device for HF/Accelerate sharded models.
    Prefer the input embedding device, then hf_device_map, then the first
    non-meta parameter device.
    """
    try:
        emb = model.get_input_embeddings()
        weight = getattr(emb, "weight", None)
        if isinstance(weight, torch.Tensor) and weight.device.type != "meta":
            return weight.device
    except Exception:
        pass

    hf_device_map = getattr(model, "hf_device_map", None)
    if isinstance(hf_device_map, dict) and hf_device_map:
        preferred_keys = [
            "model.embed_tokens",
            "embed_tokens",
            "transformer.wte",
            "wte",
            "tok_embeddings",
        ]
        for key in preferred_keys:
            for name, dev in hf_device_map.items():
                if key in str(name) and str(dev) not in {"cpu", "disk"}:
                    return torch.device(str(dev))
        for _name, dev in hf_device_map.items():
            dev_s = str(dev)
            if dev_s not in {"cpu", "disk"}:
                return torch.device(dev_s)
        for _name, dev in hf_device_map.items():
            if str(dev) == "cpu":
                return torch.device("cpu")

    for p in model.parameters():
        if p.device.type != "meta":
            return p.device
    return torch.device("cpu")


