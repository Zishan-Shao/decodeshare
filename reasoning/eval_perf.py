def _parse_max_memory_map(spec: str) -> Dict[Any, str]:
    out: Dict[Any, str] = {}
    spec = str(spec or "").strip()
    if not spec:
        return out
    for item in spec.split(","):
        item = item.strip()
        if not item:
            continue
        if ":" not in item:
            raise ValueError(f"Invalid max_memory_map entry: {item!r}")
        key_raw, val_raw = item.split(":", 1)
        key_raw = key_raw.strip()
        val_raw = val_raw.strip()
        if not key_raw or not val_raw:
            raise ValueError(f"Invalid max_memory_map entry: {item!r}")
        key: Any = "cpu" if key_raw.lower() == "cpu" else int(key_raw)
        val = val_raw if any(ch.isalpha() for ch in val_raw) else f"{int(float(val_raw))}GiB"
        out[key] = val
    return out



