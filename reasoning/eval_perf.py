def md_table(rows: List[List[str]], header: List[str]) -> str:
    out = []
    out.append("| " + " | ".join(header) + " |")
    out.append("|" + "---|" * len(header))
    for r in rows:
        out.append("| " + " | ".join(r) + " |")
    return "\n".join(out)

def latex_table(
    headers: List[str],
    rows: List[List[str]],
    caption: str = "",
    label: str = "",
    colspec: Optional[str] = None,
    escape_cells: bool = True,
) -> str:
    """
    Minimal LaTeX table generator.

    - headers/rows: plain strings (will escape LaTeX special chars by default)
    - caption: assumed to be *LaTeX-ready* (we do NOT escape it, so you can use $\\alpha$ etc.)
    - label: used as-is
    - colspec: e.g. 'lccc'
    """

    if colspec is None:
        colspec = "l" + "c" * max(0, (len(headers) - 1))

    def _esc(s: str) -> str:
        if not escape_cells:
            return s
        # escape LaTeX special chars in table cells/headers
        repl = {
            "\\": r"\textbackslash{}",
            "&": r"\&",
            "%": r"\%",
            "$": r"\$",
            "#": r"\#",
            "_": r"\_",
            "{": r"\{",
            "}": r"\}",
            "~": r"\textasciitilde{}",
            "^": r"\textasciicircum{}",
        }
        out = str(s)
        for k, v in repl.items():
            out = out.replace(k, v)
        return out

    hdr_line = " & ".join(_esc(h) for h in headers) + r" \\"
    body_lines = []
    for r in rows:
        body_lines.append(" & ".join(_esc(x) for x in r) + r" \\")
    body = "\n".join(body_lines)

    out: List[str] = []
    out.append(r"\begin{table}[t]")
    out.append(r"\centering")
    if caption:
        # caption is assumed LaTeX-ready (do not escape)
        out.append(rf"\caption{{{caption}}}")
    if label:
        out.append(rf"\label{{{label}}}")
    out.append(rf"\begin{{tabular}}{{{colspec}}}")
    out.append(r"\hline")
    out.append(hdr_line)
    out.append(r"\hline")
    out.append(body)
    out.append(r"\hline")
    out.append(r"\end{tabular}")
    out.append(r"\end{table}")
    return "\n".join(out)


